package by.tr.hw.view;

import by.tr.hw.entity.Complex;

public class ComplexView {
	public void printSum(Complex f1, Complex f2, Complex f3) {

		System.out.println(f1 + "i" + " + " + f2 + "i" + " = " + f3 + "i");

	}

	public void printMinus(Complex f1, Complex f2, Complex f3) {

		System.out.println(f1 + "i" + " - " + f2 + "i" + " = " + f3 + "i");

	}

}
